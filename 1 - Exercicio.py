# Loop de 0 a 100, incluindo o número 100
for i in range(101):
    # Verifica se o número atual é divisível por 2
    if i % 2 == 0:
        # Se for par, imprime que o número é par
        print(f"O número ({i}) é Par")
    else:
        # Se não for par, imprime que o número é ímpar
        print(f"O número ({i}) é impar")

